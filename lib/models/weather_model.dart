class WeatherModel {
  final String city;
  final String time;
  final String image;
  final double avgTemp;
  final double maxTemp;
  final double minTemp;
  final String weatherCondition;

  WeatherModel({required this.city, required this.time, required this.image, required this.avgTemp, required this.maxTemp, required this.minTemp, required this.weatherCondition});
}
